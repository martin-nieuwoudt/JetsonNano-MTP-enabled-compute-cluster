# Stage 1 + Stage 3 call the 32B sharded across all 11 Jetson nodes. This is
# the whole reason the cluster exists: a single PC cannot hold the 32B, and it
# is the model that generates the strategy DAG and writes the manuscript prose.
def _big_model_32b(prompt: str, n_predict: int = 1024) -> str:
    """Synchronous call to the 32B; wrapped for the async orchestrator."""
    from llm_client import complete, is_available
    if not is_available():
        return "[32B unavailable — is cluster_server.py running the 32B?]"
    return complete(prompt, n_predict=n_predict)


async def stage1_strategy_32b() -> list:
    """Stage 1: the 32B decomposes the Anti-Dark-Forest theory into a task DAG.

    Returns a list of (capability, model, data, deps) tuples, exactly the shape
    stage1_define_strategy expects. The 32B is asked to map the 11 propositions
    to the 11 method harnesses and declare dependencies.
    """
    prompt = (
        "You are the Strategist in a 3-phase research loop testing the "
        "'Biology as Bounded Information' (Anti-Dark-Forest) thesis on an "
        "11-node Jetson cluster. Define Stage 1 strategy: produce a DAG of 11 "
        "tasks, one per simulation method "
        "(marl, montecarlo, thermo_ca, kl_div, lean, bayesian, viability_kernel, "
        "replication_thermo, tiep_lifetime, jevons_throughput, recursive_viability). "
        "Each task: capability=MATH/CODE/REASON, model='small', data=a one-line "
        "instruction naming the method and what it must measure, deps=[] (all "
        "independent — they run in parallel on separate nodes). Reply ONLY with "
        "the 11 tasks, one per line, format: METHOD|CAP|DATA."
    )
    out = _big_model_32b(prompt, n_predict=512)
    await asyncio.sleep(0)  # keep coroutine; real I/O is sync via llm_client
    tasks = []
    for line in out.splitlines():
        line = line.strip()
        if "|" not in line:
            continue
        parts = [p.strip() for p in line.split("|")]
        if len(parts) < 3:
            continue
        method, cap, data = parts[0], parts[1], parts[2]
        tasks.append((cap, "small", f"{method}: {data}", []))
    # Fallback: if the 32B returned nothing parseable, use the canonical 11.
    if not tasks:
        methods = ["marl", "montecarlo", "thermo_ca", "kl_div", "lean", "bayesian",
                   "viability_kernel", "replication_thermo", "tiep_lifetime",
                   "jevons_throughput", "recursive_viability"]
        tasks = [(("MATH" if m in ("marl", "montecarlo", "kl_div", "bayesian")
                   else "CODE"), "small", f"{m}: execute method harness on node",
                  []) for m in methods]
    return tasks


async def stage3_prose_32b(verdict_json: str) -> str:
    """Stage 3: the 32B reads the deterministic rubric verdict and writes the
    manuscript prose (integration, what could be improved, proposed edits in
    natural language)."""
    prompt = (
        "You are the Judge in a 3-phase research loop. Below is the deterministic "
        "rubric verdict for the 'Biology as Bounded Information' thesis (Phase-3 "
        "JSON: propositions pass/fail, critique findings, blind-spot audit). "
        "Write the manuscript integration section: (1) what the evidence supports, "
        "(2) the two honest failures (P2 heuristic seeding, P6 bi-modal silence) and "
        "how to fix their metrics, (3) concrete next edits. Be precise, no fluff.\n\n"
        f"{verdict_json}"
    )
    await asyncio.sleep(0)  # keep coroutine; real I/O is sync via llm_client
    return _big_model_32b(prompt, n_predict=1500)


if __name__ == "__main__":
    async def _main():
        # Production run: Stage 1 + Stage 3 driven by the 32B on the cluster.
        tr = await run_pipeline(stage1_strategy_32b, stage3_prose_32b)
        print(f"Trace written: {TRACE_JSON}")
        print(f"Graph written: {TRACE_DOT}")
        print(f"Tasks: {len(tr.tasks)}, Gates: {len(tr.gates)}")
    asyncio.run(_main())
