async def _example_plan_fn():
    # Map the example plan to real task ids for the dependency edge.
    ids = ["TASK_MARL", "TASK_MC", "TASK_CA", "TASK_KL", "TASK_LEAN", "TASK_BAYES"]
    out = []
    for i, (cap, model, data, _deps) in enumerate(EXAMPLE_PLAN):
        deps = [] if i < 6 else ids
        out.append((cap, model, data, deps))
    await asyncio.sleep(0)
    return out


async def _example_judge_fn(summary: str) -> str:
    await asyncio.sleep(0)
    return (f"[dry-run judge] Reviewed {len(summary.splitlines())} task results. "
            f"Thesis holds pending empirical sweep.")


